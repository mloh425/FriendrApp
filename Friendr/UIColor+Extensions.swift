import UIKit

extension UIColor {
    
    class func friendrBlue() -> UIColor {
        return UIColor(red: 10.0/255.0, green: 149.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
    class func friendrOrange() -> UIColor {
        return UIColor(red: 248.0/255.0, green: 153.0/255.0, blue: 29.0/255.0, alpha: 1.0)
    }
    
    class func facebookBlue() -> UIColor {
        return UIColor(red: 59.0/255.0, green: 89.0/255.0, blue: 152.0/255.0, alpha: 1.0)
    }
    
    class func twitterBlue() -> UIColor {
        return UIColor(red: 64.0/255.0, green: 153.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
}
